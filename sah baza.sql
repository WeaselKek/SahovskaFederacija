CREATE TABLE "SUDIJA"
(
"SUDIJA_ID" NUMBER (10,0),
CONSTRAINT SUDIJA_PK PRIMARY KEY (SUDIJA_ID)

);


CREATE TABLE "SAHISTA"
(
"REG_BR" NUMBER (10,0),
"IME" VARCHAR2 (30) NOT NULL,
"PREZIME" VARCHAR2(30) NOT NULL,
"DAT_RODJ" DATE NOT NULL,
"DAT_UCLANJENJA" DATE NOT NULL,
"BROJ_PASOSA" NUMBER (10,0) NOT NULL,
"DRZAVA" VARCHAR2 (30) NOT NULL,
"ULICA" VARCHAR2 (30),
"BROJ" NUMBER (5),
"GRAD" VARCHAR2 (30),

CONSTRAINT SAHISTA_PK PRIMARY KEY (REG_BR)
);

CREATE TABLE "OBICAN_CLAN"
(
"REG_BR" NUMBER (10,0) CONSTRAINT OBIC_FK REFERENCES SAHISTA,
CONSTRAINT OBIC_PK PRIMARY KEY (REG_BR)
);

CREATE TABLE "MAJSTORSKI_KANDIDAT"
(
"REG_BR" NUMBER (10,0) CONSTRAINT KANDID_FK REFERENCES SAHISTA,
"BROJ_PARTIJA_DO_STICANJA" NUMBER (5,0) NOT NULL,
CONSTRAINT KANDID_PK PRIMARY KEY (REG_BR)
);

CREATE TABLE "MAJSTOR"
(
"REG_BR" NUMBER (10,0) CONSTRAINT MAJSTOR_FK REFERENCES SAHISTA,
"DAT_STICANJA" DATE NOT NULL,
"SUDIJA_ID" NUMBER (10,0) CONSTRAINT SUD_MAJSTOR_FK REFERENCES SUDIJA,
CONSTRAINT MAJSTOR_PK PRIMARY KEY (REG_BR)
);

CREATE TABLE "TURNIR"
(
"ID" NUMBER (10,0) ,
"NAZIV" VARCHAR2 (30) NOT NULL,
"DRZAVA" VARCHAR2 (30) NOT NULL,
"GRAD" VARCHAR2 (30) NOT NULL,
"GODINA" NUMBER (4,0) NOT NULL,
"DATUM_OD" DATE NOT NULL,
"DATUM_DO" DATE NOT NULL,

"NACIN_ODIGRAVANJA" VARCHAR2 (30) 
CHECK (NACIN_ODIGRAVANJA IN ('NORMALAN','BRZOPOTEZNI')) NOT NULL,

"TRAJANJE_PARTIJE" NUMBER(3,0),

"PO_ZNACAJU" VARCHAR2(30)
CHECK (PO_ZNACAJU IN ('TAKMICARSKI','EGZIBICIONI')) NOT NULL,

"TIP_TAKMICARSKOG" VARCHAR2(30)
CHECK (TIP_TAKMICARSKOG IN ('NACIONALNI','REGIONALNI','INTERNACIONALNI')),

"TIP_EGZIBICIONOG" VARCHAR2 (30)
CHECK (TIP_EGZIBICIONOG IN ('PROMOTIVNI','HUMANITARNI')),

"NOVAC" NUMBER (10,0),
"NAMENA_NOVCA" VARCHAR2(30),

CONSTRAINT TURNIR_PK PRIMARY KEY (ID)
);

CREATE TABLE "SPONZOR"
(
"NAZIV" VARCHAR2(30),
CONSTRAINT SPONZOR_PK PRIMARY KEY (NAZIV)
);

CREATE TABLE "SPONZORISE"
(
"ID" NUMBER (10,0),
"NAZIV_SPONZORA" VARCHAR2(30) CONSTRAINT SPONZOR_FK REFERENCES SPONZOR NOT NULL,
"TURNIR_ID" NUMBER (10,0) CONSTRAINT TURNIR_SPONZ_FK REFERENCES TURNIR NOT NULL,
CONSTRAINT SPONZORISE_PK PRIMARY KEY (ID),
CONSTRAINT UQ_SPON UNIQUE(NAZIV_SPONZORA, TURNIR_ID)
);

CREATE TABLE "PARTIJA"
(
"ID" NUMBER(20,0),
"DATUM" DATE NOT NULL,
"VREME" VARCHAR2(5),
"TRAJANJE" NUMBER (5,0),

"ISHOD" VARCHAR2(10)
CHECK (ISHOD IN ('1-0','0-1','REMI')) NOT NULL,

"TURNIR_ID" NUMBER (10,0) CONSTRAINT TURNIR_PART_FK REFERENCES TURNIR NOT NULL,
"SUDIJA_ID" NUMBER (10,0) CONSTRAINT SUD_PART_FK REFERENCES SUDIJA NOT NULL,
"BELI_IGRAC" NUMBER (10,0) CONSTRAINT BELI_IGRAC_FK REFERENCES SAHISTA NOT NULL,
"CRNI_IGRAC" NUMBER (10,0) CONSTRAINT CRNI_IGRAC_FK REFERENCES SAHISTA NOT NULL,

CONSTRAINT PARTIJA_PK PRIMARY KEY (ID)

);

CREATE TABLE "POTEZ"
(
"ID" NUMBER (10,0),
"PARTIJA_ID" NUMBER (20,0) CONSTRAINT PARTIJA_FK REFERENCES PARTIJA NOT NULL,
"REDNI_BROJ" NUMBER (3,0) NOT NULL,
"POCETNO_POLJE" VARCHAR2(2) NOT NULL,
"KRAJNJE_POLJE" VARCHAR2(5) NOT NULL,
"VREME"  VARCHAR2(5),

CONSTRAINT POTEZ_PK PRIMARY KEY (ID),
CONSTRAINT UQ_POTEZ UNIQUE(PARTIJA_ID, REDNI_BROJ)
);


CREATE TABLE "ORGANIZATOR"
(
"MAT_BR" VARCHAR2(13),
"IME" VARCHAR2 (30) NOT NULL,
"PREZIME" VARCHAR2(30) NOT NULL,
"ULICA" VARCHAR2 (30),
"BROJ" NUMBER (5),
"GRAD" VARCHAR2 (30),
"SUDIJA_ID"  NUMBER (10,0) CONSTRAINT SUD_ORG_FK REFERENCES SUDIJA,

CONSTRAINT ORGANIZATOR_PK PRIMARY KEY (MAT_BR)
);

CREATE TABLE "ORGANIZUJE"
(
"ID" NUMBER(10,0),
"ORGANIZATOR_MBR" VARCHAR2(13) CONSTRAINT ORGANIZUJE_FK REFERENCES ORGANIZATOR NOT NULL,
"TURNIR_ID" NUMBER (10,0) CONSTRAINT TURNIR_ORG_FK REFERENCES TURNIR NOT NULL,
CONSTRAINT ORGANIZUJE_PK PRIMARY KEY (ID),
CONSTRAINT UQ_ORG UNIQUE(ORGANIZATOR_MBR, TURNIR_ID)
);



